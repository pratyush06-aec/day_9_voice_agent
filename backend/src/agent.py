import logging
from merchant import list_products, create_order, load_catalog

from dotenv import load_dotenv
from livekit.agents import (
    Agent,
    AgentSession,
    JobContext,
    JobProcess,
    MetricsCollectedEvent,
    RoomInputOptions,
    WorkerOptions,
    cli,
    metrics,
    tokenize,
    function_tool,
    RunContext
)
from livekit.plugins import murf, silero, google, deepgram, noise_cancellation
from livekit.plugins.turn_detector.multilingual import MultilingualModel

logger = logging.getLogger("agent")

load_dotenv(".env.local")

class Assistant(Agent):
    def __init__(self) -> None:
        super().__init__(
            instructions="""
You are an E-commerce Game Master assistant following an ACP-inspired workflow.

Rules:
- Understand what the user wants to buy.
- Call the appropriate commerce tools to browse products or create orders.
- Describe products clearly (name + key attribute + price).
- Always confirm before placing an order.
- Maintain continuity using ctx.userdata.
- End each response with: "What would you like next?"
"""
,
        )

        @function_tool()
        async def list_products_tool(self, ctx: RunContext, filters: dict | None = None):
            products = list_products(filters)
            ctx.userdata["last_products"] = products
            return products

        @function_tool()
        async def create_order_tool(self, ctx: RunContext, product_id: str, quantity: int = 1):
            order = create_order([{"product_id": product_id, "quantity": quantity}])
            ctx.userdata["last_order"] = order
            return order

        @function_tool()
        async def get_last_order_tool(self, ctx: RunContext):
            return ctx.userdata.get("last_order", "No recent order found.")


